    public void iniciarPartida()
    {
        
        
        for (int i = 0; i < 8; i++) {
            for (int j = 0; j < 8; j++) {
                tableroMio[i][j]=0;
                tableroSuyo[i][j]=0;
            }
        }
        
        for (int tam = 0; tam <=5; tam++) {
            ponerBarco(tableroSuyo, tam);
        }
    }
    
    public void  paint(Graphics g) 
    {  
        if(primeraVez){ 
         g.drawImage(portada,0,0,this);
         g.drawImage(tablero,0,0,this);
         primeraVez=false;
      
        }
        
        if(nEstado==0){
          g.drawImage(portada,0,0,this);
        }else{
           pintarTablero(g,tableroMio,73,90);
           pintarTablero(g,tableroSuyo,485,90);
            iniciarPartida();
            g.drawImage(tablero,0,0,this);
         
        }
       
       
       
    }
    
    public void pintarTablero(Graphics g, int[][]tab,int x, int y)
    {
        for (int i = 0; i < 8; i++) {
            for (int j = 0; j <8; j++) {
                if(tab[i][j]==0){
                    g.drawRect(x+i*30, y+j*30, 30, 30);
                }
            }
        }
    }
