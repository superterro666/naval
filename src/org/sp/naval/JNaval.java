    public JNaval() {
        
        Imagenes i = new Imagenes();
        initComponents();
        portada = i.cargar("principal.jpg");
        tablero = i.cargar("tableroHundir.png");
        setBounds(0,0,800,600);
        addMouseListener(
        new MouseAdapter(){
        public void mouseClicked(MouseEvent e)
        {
            if(nEstado==0){
                nEstado=1;
                repaint();
            }
        }
        });
    }
    
